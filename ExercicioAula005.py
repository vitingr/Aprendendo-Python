n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2
pot = n1 ** n2
print('A soma dos valores {} e {}, equivale a {}'.format(n1, n2, soma))
print('A subtração dos valores {} e {}, equivale a {}'.format(n1, n2, sub))
print('A multiplicação dos valores {} e {}, equivale a {}'.format(n1, n2, mult))
print('A divisão dos valores {} e {}, equivale a {}'.format(n1, n2, div))
print('A potência dos valores {} e {}, equivale a {}'.format(n1, n2, pot))
