n = int(input('Digite um número: '))
a = n - 1
s = n + 1
d = n * 2
t = n * 3
r = n ** (1/2)
print('Você escolheu o número {}, seu antecessor é {} e seu sucessor é {}.'.format(n, a, s))
print('O dobro desse valor é {} e o triplo é {}'.format(d, t))
print('E a raiz quadrada desse valor é {}'.format(r))
